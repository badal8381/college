class Rectangle{
	private float width;
	private float length;
	private float area;
	private String color;	
	
	void setWidth(float width){
		this.width = width;
	}
	void setLength(float length){
		this.length = length;
	}
	void setArea(float area){
		this.area = area;
	}
	void setColor(String color){
		this.color = color;
	}
	
	float getWidth(){
		return width;
	}
	float getLength(){
		return length;
	}
	float getArea(){
		return area;
	}
	String getColor(){
		return color;
	}
}
