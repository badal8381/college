class SquaresAndCubes{
	public static void main(String[] args){
		System.out.print("Squares till 10 are: ");
		for(int i=1; i<10; i++){
			System.out.print((i*i)+" ");
		}	
		System.out.println();
		System.out.print("Cubes till 10 are: ");
		for(int i=1; i<10; i++){
			System.out.print((i*i*i)+" ");
		}	
		System.out.println();
	}
}
